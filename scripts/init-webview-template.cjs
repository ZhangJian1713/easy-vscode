#!/usr/bin/env node
/**
 * Generate scaffold/webview.webpack.js and optionally vendor templates into scaffold/bundler/.
 *
 * Usage:
 *   node scripts/init-webview-template.cjs --cwd /path/to/my-extension --template minimal-react
 *   node scripts/init-webview-template.cjs --cwd /path/to/my-extension --template antd-less --vendor
 *   (--vendor copies into <cwd>/scaffold/bundler/)
 *   node scripts/init-webview-template.cjs --cwd . --template minimal-react --force
 *
 * Env:
 *   EASY_VSCODE_TEMPLATES_ROOT  Absolute path to the folder that contains minimal-react/ and antd-less/
 */

const fs = require('fs')
const path = require('path')

const TEMPLATES = {
  'minimal-react': 'createMinimalReactWebviewWebpackConfig',
  'antd-less': 'createAntdLessWebviewWebpackConfig'
}

function parseArgs(argv) {
  const out = { force: false, vendor: false, checkEntry: false, cwd: null, template: null }
  for (let i = 2; i < argv.length; i++) {
    const a = argv[i]
    if (a === '--force') out.force = true
    else if (a === '--vendor') out.vendor = true
    else if (a === '--check-entry') out.checkEntry = true
    else if (a === '--cwd') out.cwd = argv[++i]
    else if (a === '--template') out.template = argv[++i]
    else if (a === '--help' || a === '-h') out.help = true
  }
  return out
}

function findTemplatesRoot(startDir) {
  if (process.env.EASY_VSCODE_TEMPLATES_ROOT) {
    const r = process.env.EASY_VSCODE_TEMPLATES_ROOT
    if (fs.existsSync(r)) return path.resolve(r)
  }
  let dir = path.resolve(startDir)
  for (let depth = 0; depth < 12; depth++) {
    const nested = path.join(dir, 'easy-vscode', 'webview-templates')
    if (fs.existsSync(nested)) return nested
    const flat = path.join(dir, 'webview-templates')
    if (fs.existsSync(path.join(flat, 'README.md'))) return flat
    const parent = path.dirname(dir)
    if (parent === dir) break
    dir = parent
  }
  return null
}

function posixRel(from, to) {
  return path.relative(from, to).split(path.sep).join('/')
}

function copyDirRecursive(src, dest, force) {
  if (!fs.existsSync(src)) throw new Error(`Missing template source: ${src}`)
  if (fs.existsSync(dest)) {
    const stat = fs.statSync(dest)
    if (stat.isDirectory()) {
      const entries = fs.readdirSync(dest).filter((e) => e !== '.gitkeep')
      if (entries.length > 0 && !force) {
        console.log(`[init-webview-template] skip copy (non-empty): ${dest}`)
        return
      }
      if (force && entries.length > 0) {
        fs.rmSync(dest, { recursive: true })
      }
    }
  }
  fs.cpSync(src, dest, { recursive: true })
  console.log(`[init-webview-template] copied template -> ${dest}`)
}

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true })
}

function main() {
  const args = parseArgs(process.argv)
  if (args.help) {
    console.log(`init-webview-template.cjs
  --cwd <dir>           Extension root (default: process.cwd())
  --template <name>     minimal-react | antd-less
  --vendor              Copy template into <cwd>/scaffold/bundler/ (overwrites when --force)
  --force               Overwrite webview.webpack.js; with --vendor, replace vendored folder
  --check-entry         Warn if src/webview/index.tsx is missing
`)
    process.exit(0)
  }

  const cwd = path.resolve(args.cwd || process.cwd())
  const template = args.template || 'minimal-react'
  if (!TEMPLATES[template]) {
    console.error(`Unknown template: ${template}. Use: ${Object.keys(TEMPLATES).join(', ')}`)
    process.exit(1)
  }

  const factoryExport = TEMPLATES[template]
  let templatesRoot = findTemplatesRoot(cwd)
  if (!templatesRoot) {
    console.error(
      'Could not find webview-templates. Set EASY_VSCODE_TEMPLATES_ROOT or run from a monorepo that contains easy-vscode/webview-templates.'
    )
    process.exit(1)
  }

  const templateSrcDir = path.join(templatesRoot, template)
  if (!fs.existsSync(path.join(templateSrcDir, 'webpack.factory.js'))) {
    console.error(`Missing factory: ${path.join(templateSrcDir, 'webpack.factory.js')}`)
    process.exit(1)
  }

  let templateBundleDir = templateSrcDir
  if (args.vendor) {
    const vendorRoot = path.join(cwd, 'scaffold', 'bundler')
    copyDirRecursive(templateSrcDir, vendorRoot, args.force)
    templateBundleDir = vendorRoot
  }

  const scaffoldDir = path.join(cwd, 'scaffold')
  const outFile = path.join(scaffoldDir, 'webview.webpack.js')

  if (fs.existsSync(outFile) && !args.force) {
    console.log(`[init-webview-template] skip write (exists): ${outFile}`)
  } else {
    ensureDir(scaffoldDir)
    const relFactory = posixRel(scaffoldDir, path.join(templateBundleDir, 'webpack.factory.js'))
    const relTemplateRoot = posixRel(scaffoldDir, templateBundleDir)
    const body = `/* Generated by easy-vscode/scripts/init-webview-template.cjs — scaffold/ = tooling + vendored bundler */
const path = require('path')
const { ${factoryExport} } = require('${relFactory}')
const context = path.resolve(__dirname, '..')
const templateRoot = path.resolve(__dirname, '${relTemplateRoot}')

module.exports = ${factoryExport}({
  context,
  entry: path.resolve(context, 'src/webview/index.tsx'),
  htmlTemplate: path.resolve(templateRoot, 'src/index.html'),
  favicon: path.resolve(templateRoot, 'src/favicon.ico'),
  babelConfig: path.resolve(context, '.babelrc')
})
`
    fs.writeFileSync(outFile, body, 'utf8')
    console.log(`[init-webview-template] wrote ${outFile}`)
  }

  const entryPath = path.join(cwd, 'src/webview/index.tsx')
  if (args.checkEntry || !fs.existsSync(entryPath)) {
    if (!fs.existsSync(entryPath)) {
      console.log(`[init-webview-template] hint: create entry ${entryPath} (registerWebview + your panels)`)
    }
  }

  console.log('[init-webview-template] done.')
}

main()
